    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double imcUsuario = 0, peso, altura;

        if (!"".equals(txtPeso.getText()) && !"".equals(txtAltura.getText())) {
            peso = Double.parseDouble(txtPeso.getText());
            altura = Double.parseDouble(txtAltura.getText());
            imcUsuario = peso / (altura * altura);
            lblResultado.setText("Seu IMC Ã© : " + imcUsuario);
        } else {
            JOptionPane.showMessageDialog(this, "Informe os valors de peso e altura!", "Campoes em branco", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnCalcularActionPerformed

